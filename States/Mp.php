                            <h4>1. Great Stupa</h4>
                            <ul>
                                <li>Oldest stone structure in India</li>
                                <li>Contains relics of Buddha</li>
                                <li>Famous for its four ornate gateways (toranas)</li>
                            </ul>
                            
                            <h4>2. Sanchi Archaeological Museum</h4>
                            <ul>
                                <li>Displays artifacts found at the site</li>
                                <li>Includes the lion capital of Ashoka</li>
